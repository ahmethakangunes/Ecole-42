#include "minishell.h"

int	main(int argc, char **argv, char **env)
{
	argc = 0;
	argv = 0;
	g_shell = malloc(sizeof(g_shell));


	g_shell->env = env;
	while (1)
	{
		g_shell->command = readline(ft_prompt());
	}
	return (0);
}
